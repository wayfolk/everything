///////////////////
///// IMPORTS /////
///////////////////

/// NPM
import
{
  getGPUTier,
}
from 'detect-gpu';

///////////////
///// OBJ /////
///////////////

const ENV = Object.create(null);

///////////////////////
///// OBJ METHODS /////
///////////////////////

ENV.detectGPU = async function(fCB)
{
  this.gpu = await getGPUTier
  (
    {
      benchmarksURL: '/_assets/_benchmarks',
    }
  );

  if (fCB) fCB();
};

ENV.getGPU = function()
{
  return this.gpu;
};

ENV.getCssBreakpoint = function()
{
  const nWindowWidth = window.innerWidth;

  if (nWindowWidth <= 575.98) { return "xs"; }
  else if (nWindowWidth <= 1199.98) { return "s"; }
  else if (nWindowWidth <= 1399.98) { return "m"; }
  else { return "l"; }
};

//////////////////////
///// ES6 EXPORT /////
//////////////////////

export
{
  ENV
};

